# Faça um programa que leia um número Inteiro e mostre na tela o seu sucessor e seu antecessor.

number = int(input('Olá novamente! Vamos lá! Digite um número inteiro e te diremos seu sucessor e antecessor: '))
print(f'O número digitado foi {number}, sendo seu sucessor {number + 1} e seu antecessor {number - 1}.')